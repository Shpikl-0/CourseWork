#include <iostream>
using namespace std;

int main()
{
    int matrix[9][9] = {{1,1,4,3,4,1,3,2,2},
                        {1,1,2,3,2,1,3,2,2},
                        {3,2,1,4,3,3,2,1,3},
                        {4,3,4,2,3,1,1,2,4},
                        {4,2,1,1,2,3,3,4,1},
                        {2,2,3,3,4,4,4,1,2},
                        {2,3,3,1,3,2,2,4,1},
                        {4,4,2,1,3,1,2,3,3},
                        {4,4,2,1,1,1,2,3,3}};

    for(int i = 0; i < 9; i++)
    {
        for(int j = 0; j < 9; j++)
        {
            cout << matrix[i][j] << " ";
        }
        cout << '\n';
    }
    return 0;
}